        'קוד נכון. הָקִישִׁי קוד חדש בן ארבע ספרות.',
        'קוד שגוי. נסה שוב. הָקֵשׁ את קוד הכניסה הנוכחי.',
        'קוד שגוי. נסי שוב. הָקִישִׁי את קוד הכניסה הנוכחי.',
